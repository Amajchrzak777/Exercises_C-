#include <iostream>

using namespace std;

int main()
{
    int x=57;
    cout << "The number is: "<<x<< endl;
    x++;
    cout<<x<<endl;
    ++x;
    cout <<x<<endl;
    x=x+1;
    cout <<x<<endl;
    x--;
    cout <<x<<endl;
    --x;
    cout <<x<<endl;
    x=x-1;
    cout <<x<<endl;
    return 0;
}
